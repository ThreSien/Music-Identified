    public void playBack() {

        Buttonstart = new PlayingTimer(labelTimeCounter, sliderTime);
        Buttonstart.start();
        isPlaying = true;
        playbackThread = new Thread(new Runnable() {

            @Override
            public void run() {
                // TODO Auto-generated method stub
                try {
                    String selectedSongName = jList1.getSelectedValue();
                    loadDatabaseMusic(selectedSongName);
                    
//                    System.out.println("startTime"+startTime);
                    player.loadMusic(filepath);
                   
                    Buttonstart.setAudioClip(player.getClip_ToPlayingTimer());
                    TotalTime.setText(player.getClipLengthString());
                    sliderTime.setMaximum(player.getClip_ToPlayingTimer().getFrameLength() / 48_000);
                    
                    player.playerStart();
                    resetControlls();
                } catch (UnsupportedAudioFileException ex) {
                    JOptionPane.showMessageDialog(null, "The audio format is unsupported!", "Error",
                            JOptionPane.ERROR_MESSAGE);
//                    ex.printStackTrace();

                    resetControlls();
                } catch (LineUnavailableException ex) {
                    JOptionPane.showMessageDialog(null, "Could not play the audio file because line is unavailable!",
                            "Error", JOptionPane.ERROR_MESSAGE);

//                    ex.printStackTrace();

                    resetControlls();
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "I/O error while playing the audio file!", "Error",
                            JOptionPane.ERROR_MESSAGE);

//                    ex.printStackTrace();

                    resetControlls();
                } catch (Exception ex) {
                    
                }
            }
        });
        playbackThread.start();
    }

