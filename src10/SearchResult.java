    private void resetControlls() {
        Buttonstart.reset();
        Buttonstart.interrupt();
        isPlaying = false;
    }

    private void stopPlaying() {
        isPause = false;
        Buttonstart.reset();
        Buttonstart.interrupt();
        player.stop();
        playbackThread.interrupt();
    }

    private void pausePlayer() {
        isPause = true;
        player.pause();
        Buttonstart.pauseTimer();
        playbackThread.interrupt();
    }

    private void resumePlayer() {
        isPause = false;
        player.resume();
        Buttonstart.resumeTimer();
        playbackThread.interrupt();
    }

